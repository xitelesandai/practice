# 集合
# 定义集合：set()
# 函数：add(值)--集合末尾追加元素
# 函数：update(值)--集合末尾批量追加元素
# 函数：discard(值)--集合按照值删除元素
# 函数：remove(值)--集合按照值删除元素(没有值时会报错)
set3 = set((1, 2, 3, 3, 2, 1))
print(set3)
print(set3.pop())
print(set3)

# 字典
# 定义字典：{'键':'值'}
# 函数：update(键1=值1,键2=值2,...)--批量添加字典
# 函数：get(键)--通过键获取对应的值但是可以设置默认值
# 函数：popitem()--删除字典中的元素
# 函数：pop(键,默认值)--删除并返回字典中的元素
# 函数：clear()--清空字典
scores = {'骆昊': 95, '白元芳': 78, '狄仁杰': 82}
scores.update(冷面=67, 方启鹤=85)
scores.popitem()
scores.pop('骆昊', 100)
for elem in scores:
    print('%s\t--->\t%d' % (elem, scores[elem]))


